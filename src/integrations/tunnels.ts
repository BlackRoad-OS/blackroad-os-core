// TAILSCALE FUNNEL CLIENT
// =============================================================================

export interface TailscaleConfig {
  authKey?: string;
}

export class TailscaleClient {
  private readonly config: TailscaleConfig;

  constructor(config: TailscaleConfig = {}) {
    this.config = config;
  }

  /**
   * Generate Tailscale Funnel command
   */
  generateFunnelCommand(port: number, options?: { background?: boolean; https?: boolean }): string {
    let cmd = `tailscale funnel`;

    if (options?.https !== false) {
      cmd += ` --https=${port}`;
    } else {
      cmd += ` ${port}`;
    }

    if (options?.background) {
      cmd += ` --bg`;
    }

    return cmd;
  }

  /**
   * Generate Tailscale Serve command (local network only)
   */
  generateServeCommand(port: number, options?: { https?: boolean; path?: string }): string {
    let cmd = `tailscale serve`;

    if (options?.https !== false) {
      cmd += ` https:${port}`;
    } else {
      cmd += ` ${port}`;
    }

    if (options?.path) {
      cmd += ` ${options.path}`;
    }

    return cmd;
  }

  /**
   * Generate installation script
   */
  generateInstallScript(): string {
    return `#!/bin/bash
# Tailscale Installation Script
# Generated by BlackRoad OS

set -e

echo "Installing Tailscale..."

# Detect OS
if [ -f /etc/debian_version ]; then
    curl -fsSL https://tailscale.com/install.sh | sh
elif [ -f /etc/redhat-release ]; then
    curl -fsSL https://tailscale.com/install.sh | sh
elif [ "$(uname)" == "Darwin" ]; then
    brew install tailscale
else
    curl -fsSL https://tailscale.com/install.sh | sh
fi

echo "Starting Tailscale..."
sudo tailscale up${this.config.authKey ? ` --authkey=${this.config.authKey}` : ''}

echo "Tailscale installed and started!"
tailscale status
`;
  }
}

