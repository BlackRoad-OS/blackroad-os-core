// CLOUDFLARE TUNNEL CLIENT
// =============================================================================

export interface CloudflareTunnelConfig {
  accountId: string;
  tunnelSecret?: string;
  tunnelId?: string;
  tunnelName?: string;
}

export class CloudflareTunnelClient {
  private readonly config: CloudflareTunnelConfig;
  private readonly apiToken: string;

  constructor(apiToken: string, config: CloudflareTunnelConfig) {
    this.apiToken = apiToken;
    this.config = config;
  }

  /**
   * Generate cloudflared config YAML
   */
  generateConfig(ingress: { hostname: string; service: string; path?: string }[]): string {
    const config = {
      tunnel: this.config.tunnelId || this.config.tunnelName,
      'credentials-file': `/root/.cloudflared/${this.config.tunnelId}.json`,
      ingress: [
        ...ingress.map((i) => ({
          hostname: i.hostname,
          service: i.service,
          ...(i.path && { path: i.path }),
        })),
        { service: 'http_status:404' },
      ],
    };

    return `# Cloudflare Tunnel Configuration
# Generated by BlackRoad OS

tunnel: ${config.tunnel}
credentials-file: ${config['credentials-file']}

ingress:
${ingress.map((i) => `  - hostname: ${i.hostname}
    service: ${i.service}${i.path ? `\n    path: ${i.path}` : ''}`).join('\n')}
  - service: http_status:404
`;
  }

  /**
   * Generate Docker Compose for cloudflared
   */
  generateDockerCompose(tunnelToken: string): string {
    return `version: '3.8'

services:
  cloudflared:
    image: cloudflare/cloudflared:latest
    container_name: cloudflared
    restart: unless-stopped
    command: tunnel --no-autoupdate run --token ${tunnelToken}
    networks:
      - tunnel-network

networks:
  tunnel-network:
    driver: bridge
`;
  }

  /**
   * Generate systemd service for cloudflared
   */
  generateSystemdService(tunnelToken: string): string {
    return `[Unit]
Description=Cloudflare Tunnel
After=network.target

[Service]
Type=simple
User=root
ExecStart=/usr/local/bin/cloudflared tunnel --no-autoupdate run --token ${tunnelToken}
Restart=always
RestartSec=5

[Install]
WantedBy=multi-user.target
`;
  }

  /**
   * Generate installation script
   */
  generateInstallScript(): string {
    return `#!/bin/bash
# Cloudflare Tunnel Installation Script
# Generated by BlackRoad OS

set -e

echo "Installing cloudflared..."

# Detect OS
if [ -f /etc/debian_version ]; then
    # Debian/Ubuntu
    curl -L --output cloudflared.deb https://github.com/cloudflare/cloudflared/releases/latest/download/cloudflared-linux-amd64.deb
    sudo dpkg -i cloudflared.deb
    rm cloudflared.deb
elif [ -f /etc/redhat-release ]; then
    # RHEL/CentOS/Fedora
    curl -L --output cloudflared.rpm https://github.com/cloudflare/cloudflared/releases/latest/download/cloudflared-linux-x86_64.rpm
    sudo rpm -i cloudflared.rpm
    rm cloudflared.rpm
elif [ "$(uname)" == "Darwin" ]; then
    # macOS
    brew install cloudflared
else
    # Generic Linux
    curl -L --output cloudflared https://github.com/cloudflare/cloudflared/releases/latest/download/cloudflared-linux-amd64
    sudo mv cloudflared /usr/local/bin/
    sudo chmod +x /usr/local/bin/cloudflared
fi

echo "cloudflared installed successfully!"
cloudflared version
`;
  }
}

