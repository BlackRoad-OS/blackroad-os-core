// NGROK TUNNEL CLIENT
// =============================================================================

export interface NgrokConfig {
  authToken: string;
  region?: 'us' | 'eu' | 'ap' | 'au' | 'sa' | 'jp' | 'in';
}

export class NgrokClient {
  private readonly config: NgrokConfig;
  private readonly baseUrl = 'https://api.ngrok.com';

  constructor(config: NgrokConfig) {
    this.config = config;
  }

  /**
   * Generate ngrok config YAML
   */
  generateConfig(tunnels: Record<string, { proto: string; addr: string | number; hostname?: string }>): string {
    return `# ngrok configuration
# Generated by BlackRoad OS

version: "2"
authtoken: ${this.config.authToken}
${this.config.region ? `region: ${this.config.region}` : ''}

tunnels:
${Object.entries(tunnels).map(([name, config]) => `  ${name}:
    proto: ${config.proto}
    addr: ${config.addr}${config.hostname ? `\n    hostname: ${config.hostname}` : ''}`).join('\n')}
`;
  }

  /**
   * Generate ngrok command for quick tunnel
   */
  generateCommand(options: {
    port: number;
    protocol?: 'http' | 'tcp' | 'tls';
    subdomain?: string;
    hostname?: string;
    auth?: string;
  }): string {
    let cmd = `ngrok ${options.protocol || 'http'} ${options.port}`;

    if (options.subdomain) {
      cmd += ` --subdomain=${options.subdomain}`;
    }
    if (options.hostname) {
      cmd += ` --hostname=${options.hostname}`;
    }
    if (options.auth) {
      cmd += ` --basic-auth="${options.auth}"`;
    }
    if (this.config.region) {
      cmd += ` --region=${this.config.region}`;
    }

    return cmd;
  }

  /**
   * Generate Docker command for ngrok
   */
  generateDockerCommand(port: number, protocol = 'http'): string {
    return `docker run -it --rm \\
  -e NGROK_AUTHTOKEN=${this.config.authToken} \\
  --network host \\
  ngrok/ngrok ${protocol} ${port}`;
  }
}

