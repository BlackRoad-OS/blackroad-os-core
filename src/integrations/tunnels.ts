/**
 * Create a tunnel manager with all providers
 */
export function createTunnelManager(): TunnelManager {
  return new TunnelManager();
}

/**
 * Create Cloudflare Tunnel client from environment
 */
export function createCloudflareTunnel(): CloudflareTunnelClient {
  const apiToken = process.env.CLOUDFLARE_API_TOKEN;
  const accountId = process.env.CLOUDFLARE_ACCOUNT_ID;

  if (!apiToken || !accountId) {
    throw new Error('CLOUDFLARE_API_TOKEN and CLOUDFLARE_ACCOUNT_ID environment variables required');
  }

  return new CloudflareTunnelClient(apiToken, {
    accountId,
    tunnelId: process.env.CLOUDFLARE_TUNNEL_ID,
    tunnelName: process.env.CLOUDFLARE_TUNNEL_NAME,
  });
}

/**
 * Create ngrok client from environment
 */
export function createNgrokClient(): NgrokClient {
  const authToken = process.env.NGROK_AUTHTOKEN;

  if (!authToken) {
    throw new Error('NGROK_AUTHTOKEN environment variable required');
  }

  return new NgrokClient({
    authToken,
    region: process.env.NGROK_REGION as NgrokConfig['region'],
  });
}
