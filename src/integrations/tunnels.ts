// LOCALTUNNEL CLIENT
// =============================================================================

export interface LocaltunnelConfig {
  subdomain?: string;
  host?: string;
}

export class LocaltunnelClient {
  private readonly config: LocaltunnelConfig;

  constructor(config: LocaltunnelConfig = {}) {
    this.config = {
      host: config.host || 'https://localtunnel.me',
      subdomain: config.subdomain,
    };
  }

  /**
   * Generate localtunnel command
   */
  generateCommand(port: number, options?: { subdomain?: string; localhost?: string }): string {
    let cmd = `lt --port ${port}`;

    if (options?.subdomain || this.config.subdomain) {
      cmd += ` --subdomain ${options?.subdomain || this.config.subdomain}`;
    }
    if (options?.localhost) {
      cmd += ` --local-host ${options.localhost}`;
    }
    if (this.config.host !== 'https://localtunnel.me') {
      cmd += ` --host ${this.config.host}`;
    }

    return cmd;
  }

  /**
   * Generate npm script for package.json
   */
  generateNpmScript(port: number, subdomain?: string): object {
    return {
      scripts: {
        tunnel: `lt --port ${port}${subdomain ? ` --subdomain ${subdomain}` : ''}`,
      },
    };
  }

  /**
   * Generate Node.js code for programmatic usage
   */
  generateNodeCode(port: number, subdomain?: string): string {
    return `const localtunnel = require('localtunnel');

async function startTunnel() {
  const tunnel = await localtunnel({
    port: ${port},
    ${subdomain ? `subdomain: '${subdomain}',` : ''}
    host: '${this.config.host}',
  });

  console.log('Tunnel URL:', tunnel.url);

  tunnel.on('close', () => {
    console.log('Tunnel closed');
  });

  tunnel.on('error', (err) => {
    console.error('Tunnel error:', err);
  });

  return tunnel;
}

startTunnel();
`;
  }
}

