/**
 * Tunnel Integrations
 *
 * Unified tunnel management for exposing local services.
 * Supports multiple tunnel providers for secure remote access.
 *
 * Features:
 * - Cloudflare Tunnels (cloudflared)
 * - ngrok tunnels
 * - localtunnel
 * - Tailscale Funnel
 * - SSH port forwarding
 */

// =============================================================================
// TYPES
// =============================================================================

export type TunnelProvider = 'cloudflare' | 'ngrok' | 'localtunnel' | 'tailscale' | 'ssh';

export interface TunnelConfig {
  provider: TunnelProvider;
  localPort: number;
  localHost?: string;
  protocol?: 'http' | 'https' | 'tcp' | 'tls';
  subdomain?: string;
  domain?: string;
  authToken?: string;
}

export interface TunnelInfo {
  id: string;
  provider: TunnelProvider;
  publicUrl: string;
  localUrl: string;
  status: 'connected' | 'connecting' | 'disconnected' | 'error';
  createdAt: Date;
  metrics?: {
    connections: number;
    bytesIn: number;
    bytesOut: number;
  };
}

