// SSH TUNNEL CLIENT
// =============================================================================

export interface SSHTunnelConfig {
  host: string;
  user: string;
  port?: number;
  keyFile?: string;
}

export class SSHTunnelClient {
  private readonly config: SSHTunnelConfig;

  constructor(config: SSHTunnelConfig) {
    this.config = {
      ...config,
      port: config.port || 22,
    };
  }

  /**
   * Generate local port forward command
   */
  localForward(localPort: number, remoteHost: string, remotePort: number): string {
    let cmd = `ssh -N -L ${localPort}:${remoteHost}:${remotePort}`;

    if (this.config.port !== 22) {
      cmd += ` -p ${this.config.port}`;
    }
    if (this.config.keyFile) {
      cmd += ` -i ${this.config.keyFile}`;
    }

    cmd += ` ${this.config.user}@${this.config.host}`;

    return cmd;
  }

  /**
   * Generate remote port forward command
   */
  remoteForward(remotePort: number, localHost: string, localPort: number): string {
    let cmd = `ssh -N -R ${remotePort}:${localHost}:${localPort}`;

    if (this.config.port !== 22) {
      cmd += ` -p ${this.config.port}`;
    }
    if (this.config.keyFile) {
      cmd += ` -i ${this.config.keyFile}`;
    }

    cmd += ` ${this.config.user}@${this.config.host}`;

    return cmd;
  }

  /**
   * Generate dynamic SOCKS proxy command
   */
  dynamicProxy(localPort: number): string {
    let cmd = `ssh -N -D ${localPort}`;

    if (this.config.port !== 22) {
      cmd += ` -p ${this.config.port}`;
    }
    if (this.config.keyFile) {
      cmd += ` -i ${this.config.keyFile}`;
    }

    cmd += ` ${this.config.user}@${this.config.host}`;

    return cmd;
  }

  /**
   * Generate SSH config entry
   */
  generateSSHConfig(name: string, tunnels?: {
    localForward?: { localPort: number; remoteHost: string; remotePort: number }[];
    remoteForward?: { remotePort: number; localHost: string; localPort: number }[];
  }): string {
    let config = `Host ${name}
  HostName ${this.config.host}
  User ${this.config.user}
  Port ${this.config.port}`;

    if (this.config.keyFile) {
      config += `\n  IdentityFile ${this.config.keyFile}`;
    }

    tunnels?.localForward?.forEach((fwd) => {
      config += `\n  LocalForward ${fwd.localPort} ${fwd.remoteHost}:${fwd.remotePort}`;
    });

    tunnels?.remoteForward?.forEach((fwd) => {
      config += `\n  RemoteForward ${fwd.remotePort} ${fwd.localHost}:${fwd.localPort}`;
    });

    return config;
  }

  /**
   * Generate autossh command for persistent tunnels
   */
  autosshCommand(localPort: number, remoteHost: string, remotePort: number): string {
    let cmd = `autossh -M 0 -o "ServerAliveInterval 30" -o "ServerAliveCountMax 3" -N -L ${localPort}:${remoteHost}:${remotePort}`;

    if (this.config.port !== 22) {
      cmd += ` -p ${this.config.port}`;
    }
    if (this.config.keyFile) {
      cmd += ` -i ${this.config.keyFile}`;
    }

    cmd += ` ${this.config.user}@${this.config.host}`;

    return cmd;
  }
}

// =============================================================================
// UNIFIED TUNNEL MANAGER
// =============================================================================

export class TunnelManager {
  private cloudflare?: CloudflareTunnelClient;
  private ngrok?: NgrokClient;
  private localtunnel?: LocaltunnelClient;
  private tailscale?: TailscaleClient;
  private ssh?: SSHTunnelClient;

  /**
   * Configure Cloudflare Tunnel
   */
  configureCloudflare(apiToken: string, config: CloudflareTunnelConfig): this {
    this.cloudflare = new CloudflareTunnelClient(apiToken, config);
    return this;
  }

  /**
   * Configure ngrok
   */
  configureNgrok(config: NgrokConfig): this {
    this.ngrok = new NgrokClient(config);
    return this;
  }

  /**
   * Configure localtunnel
   */
  configureLocaltunnel(config?: LocaltunnelConfig): this {
    this.localtunnel = new LocaltunnelClient(config);
    return this;
  }

  /**
   * Configure Tailscale
   */
  configureTailscale(config?: TailscaleConfig): this {
    this.tailscale = new TailscaleClient(config);
    return this;
  }

  /**
   * Configure SSH tunnel
   */
  configureSSH(config: SSHTunnelConfig): this {
    this.ssh = new SSHTunnelClient(config);
    return this;
  }

  /**
   * Get configured provider clients
   */
  getProviders(): {
    cloudflare?: CloudflareTunnelClient;
    ngrok?: NgrokClient;
    localtunnel?: LocaltunnelClient;
    tailscale?: TailscaleClient;
    ssh?: SSHTunnelClient;
  } {
    return {
      cloudflare: this.cloudflare,
      ngrok: this.ngrok,
      localtunnel: this.localtunnel,
      tailscale: this.tailscale,
      ssh: this.ssh,
    };
  }

  /**
   * Generate quick start command for a provider
   */
  quickStart(provider: TunnelProvider, port: number): string {
    switch (provider) {
      case 'cloudflare':
        return `cloudflared tunnel --url http://localhost:${port}`;
      case 'ngrok':
        return this.ngrok?.generateCommand({ port }) || `ngrok http ${port}`;
      case 'localtunnel':
        return this.localtunnel?.generateCommand(port) || `lt --port ${port}`;
      case 'tailscale':
        return this.tailscale?.generateFunnelCommand(port) || `tailscale funnel ${port}`;
      case 'ssh':
        return this.ssh?.localForward(port, 'localhost', port) || `ssh -L ${port}:localhost:${port} user@host`;
      default:
        throw new Error(`Unknown tunnel provider: ${provider}`);
    }
  }
}

